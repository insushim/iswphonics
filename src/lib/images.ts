/**
 * Unsplash Source API를 사용한 이미지 URL 생성
 * API 키 불필요, 무료로 사용 가능
 *
 * @param query 검색어 (영어)
 * @param width 이미지 너비
 * @param height 이미지 높이
 */
export function getUnsplashImage(query: string, width: number = 400, height: number = 300): string {
  // Unsplash Source API (deprecated but still works for simple use)
  // 더 안정적인 대안: Lorem Picsum 또는 직접 URL 매핑
  const encodedQuery = encodeURIComponent(query);
  return `https://source.unsplash.com/${width}x${height}/?${encodedQuery}`;
}

/**
 * Pexels 이미지 검색 (API 키 필요시 사용)
 */
export function getPexelsImage(query: string): string {
  // Pexels는 API 키가 필요하므로 대체 사용
  return getUnsplashImage(query);
}

