/**
 * 단어에 해당하는 이미지 URL 가져오기
 * 매핑된 이미지가 없으면 Unsplash 동적 검색 사용
 */
export function getWordImage(word: string): string {
  const normalizedWord = word.toLowerCase().trim();

  // 직접 매핑된 이미지가 있으면 사용
  if (WORD_IMAGE_MAP[normalizedWord]) {
    return WORD_IMAGE_MAP[normalizedWord];
  }

  // 없으면 Unsplash 동적 검색 (폴백)
  return getUnsplashImage(normalizedWord);
}

/**
 * 이미지 프리로드 (성능 최적화)
 */
export function preloadImage(url: string): Promise<void> {
  return new Promise((resolve, reject) => {
    const img = new Image();
    img.onload = () => resolve();
    img.onerror = reject;
    img.src = url;
  });
}

/**
 * 여러 이미지 프리로드
 */
export async function preloadImages(urls: string[]): Promise<void> {
  await Promise.allSettled(urls.map(preloadImage));
}

